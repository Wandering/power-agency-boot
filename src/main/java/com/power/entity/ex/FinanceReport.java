package com.power.entity.ex;

public class FinanceReport {
	private String d_total_fee;
	private String m_total_fee;
	private String account_balance;
	private String account_deposit;
	public String getD_total_fee() {
		return d_total_fee;
	}
	public void setD_total_fee(String d_total_fee) {
		this.d_total_fee = d_total_fee;
	}
	public String getM_total_fee() {
		return m_total_fee;
	}
	public void setM_total_fee(String m_total_fee) {
		this.m_total_fee = m_total_fee;
	}
	public String getAccount_balance() {
		return account_balance;
	}
	public void setAccount_balance(String account_balance) {
		this.account_balance = account_balance;
	}
	public String getAccount_deposit() {
		return account_deposit;
	}
	public void setAccount_deposit(String account_deposit) {
		this.account_deposit = account_deposit;
	}
}
